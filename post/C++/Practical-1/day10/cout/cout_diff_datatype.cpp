#include <iostream>

int main() {
    bool   isTrue = true;
    char   ch     = 'A';
    int    age    = 30;
    double salary = 55000.50;

    std::cout << "Boolean: " << isTrue << std::endl;
    std::cout << "Character: " << ch << std::endl;
    std::cout << "Age: " << age << std::endl;
    std::cout << "Salary: " << salary << std::endl;
    return 0;
}